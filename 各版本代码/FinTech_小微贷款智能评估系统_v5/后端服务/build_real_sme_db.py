def build_sme_database(n_per_industry: int = 100) -> dict:
    """按行业均匀生成中小微企业"""
    all_enterprises = {}

    for industry, config in INDUSTRY_CONFIG.items():
        patterns = COMPANY_NAME_PATTERNS[industry]
        for i in range(n_per_industry):
            # 选择城市
            city, district = random.choice(ALL_CITIES)

            # 生成企业名
            prefix = random.choice(patterns["prefix"])
            suffix = random.choice(patterns["suffix"])
            legal_person = random.choice(FAMILY_NAMES) + random.choice(GIVEN_NAMES_M + GIVEN_NAMES_F)
            name = f"{city}{district}{prefix}{suffix}"

            # 避免重名
            while name in all_enterprises:
                name = f"{city}{district}{prefix}{random.choice(patterns['prefix'])}{suffix}"

            # 注册资本
            cap_min, cap_max = config["capital"]
            capital = round(np.random.lognormal(mean=np.log((cap_min+cap_max)/2), sigma=1.0))
            capital = max(cap_min, min(cap_max, capital))

            # 月营收估算 (万元)
            revenue_monthly_wan = max(1, round(capital * config["revenue_mult"] * random.uniform(0.4, 2.5), 1))
            revenue_monthly = int(revenue_monthly_wan * 10000)  # 转为元

            # 利润率
            profit_margin = round(random.uniform(0.04, 0.35), 2)

            # 员工
            employees = max(1, min(500, int(capital * config["emp_per_capital"] * random.uniform(0.5, 2.0))))

            # 其他属性
            years = round(random.uniform(config["years"][0], config["years"][1]), 1)
            has_estate = random.random() < config["estate_pct"]
            tax_level = random.choice(["A","B","B","M","M","M","C","C"])
            has_license = random.random() < 0.95
            has_bank_flow = random.random() < (0.80 if revenue_monthly > 50000 else 0.40)
            is_tech = "科技" in suffix
            is_ecommerce = "电子商务" in suffix or "电商" in suffix or random.random() < 0.10
            credit_ok = random.random() < config["credit_ok"]
            overdue = min(10, int(np.random.exponential(0.8))) if not credit_ok else min(2, int(np.random.exponential(0.3)))

            # 经营范围描述
            scope_templates = {
                "manufacturing": ["生产销售"+random.choice(["五金制品","机械设备","电子元器件","塑料制品","金属材料","纺织品","包装材料"]),
                                  "加工制造"+random.choice(["汽车配件","模具","电器","建材","化工产品"]),
                                  "研发生产销售"+random.choice(["精密零部件","自动化设备","电子产品","医疗器械配件"])],
                "wholesale_retail": ["批发零售"+random.choice(["日用百货","服装鞋帽","文体用品","食品饮料","化妆品","电子产品"]),
                                    "销售"+random.choice(["办公用品","家用电器","建筑材料","五金交电","工艺品"]),
                                    "线上线下销售"+random.choice(["家居用品","母婴产品","数码产品","农副产品"])],
                "hospitality_food": ["餐饮服务，食品经营", "酒店管理，住宿服务，餐饮服务",
                                    "快餐服务，外卖配送", "热食类食品制售，冷食类食品制售"],
                "it_tech": ["软件开发，信息技术咨询服务", "网络技术服务，数据处理服务",
                           "人工智能应用软件开发", "互联网信息服务，电子商务技术服务"],
                "agriculture": ["农作物种植，农产品销售", "畜禽养殖，饲料销售",
                               "农业技术开发，技术咨询服务", "花卉苗木种植销售，园林绿化"],
                "construction": ["建筑工程施工，室内外装饰装修", "市政公用工程施工",
                                "建筑安装工程，钢结构工程", "园林绿化工程施工"],
                "transportation": ["道路货物运输，仓储服务", "物流信息咨询，供应链管理",
                                  "快递服务，货运代理", "冷链运输，城市配送"],
                "culture_sports": ["文化艺术交流活动策划", "广告设计制作发布",
                                  "摄影摄像服务", "健身服务，体育赛事策划"],
                "resident_service": ["家政服务，保洁服务", "家电维修，水电安装",
                                    "汽车美容服务，汽车维修", "物业管理服务"],
                "healthcare": ["医疗服务，诊所服务", "健康管理咨询", "口腔科医疗服务", "中医科医疗服务"],
                "education": ["教育培训，教育咨询服务", "艺术培训，体育培训", "职业技能培训", "托育服务"],
            }
            scope = random.choice(scope_templates.get(industry, ["一般经营项目"]))

            # 风险分析
            risks = []
            if capital < 20: risks.append("注册资本偏低，银行授信额度受限")
            if years < 1: risks.append("成立不足1年，多数银行需要1年以上经营记录")
            if overdue >= 3: risks.append(f"征信存在{overdue}次逾期记录，国有大行可能拒贷")
            elif overdue >= 1: risks.append(f"有{overdue}次逾期记录，需关注")
            if not has_estate: risks.append("无自有房产，纯信用贷款额度受限")
            if not has_bank_flow: risks.append("无规范对公银行流水，需沉淀6个月以上")
            if tax_level in ["C","D"]: risks.append(f"纳税等级{tax_level}级，建议提升至B级以上")
            if not risks: risks.append("经营状况正常，风险可控")

            # 描述
            desc = f"注册资本{capital}万元，{scope[:60]}。法定代表人{legal_person}。"

            all_enterprises[name] = {
                "industry": industry,
                "region": city,
                "district": district,
                "legal_person": legal_person,
                "capital_wan": capital,
                "years": years,
                "employees": employees,
                "revenue_monthly": revenue_monthly,
                "profit_margin": profit_margin,
                "has_estate": has_estate,
                "tax_level": tax_level,
                "has_license": has_license,
                "has_bank_flow": has_bank_flow,
                "is_tech": is_tech,
                "is_ecommerce": is_ecommerce,
                "credit_ok": credit_ok,
                "overdue": overdue,
                "description": desc,
                "scope": scope,
                "risk_notes": risks,
            }

    return all_enterprises


